import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MIGRATIONS_DIR = join(__dirname, '../migrations');
const OUT_CSV = join(__dirname, '../../../../docs/temp/teachers_created.csv');

const BASE = 'https://www.sut.ru/studentu/raspisanie/raspisanie-zanyatiy-studentov-ochnoy-i-vecherney-form-obucheniya';

const UA = 'Mozilla/5.0 (compatible; supreme-infra-schedule-seed/1.0)';

/** Monday dates aligned with seed semester / cycle_anchor_date */
const WEEK1_MONDAY = '2026-03-23';
const WEEK2_MONDAY = '2026-03-30';

function seedFileHeader(title) {
  return [
    `-- Seed mock data (core-schedule): ${title}`,
    `-- Generated by scripts/scrape-sut-schedule.mjs; anchor weeks ${WEEK1_MONDAY} / ${WEEK2_MONDAY}`,
    '',
  ].join('\n');
}

