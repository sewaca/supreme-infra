function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function fetchText(url) {
  const res = await fetch(url, { headers: { 'User-Agent': UA } });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.text();
}

function sqlStr(s) {
  if (s === null || s === undefined) return 'NULL';
  return `'${String(s).replaceAll("'", "''").trim()}'`;
}

/** Уникальная подпись аудитории для classroom_name и classroom.name: «корпус, номер». */
function formatClassroomLabel(building, room) {
  const r = (room || '').trim();
  const b = (building || '').trim();
  if (!r) return null;
  if (!b) return r;
  return `${b}, ${r}`;
}

function extractPrograms(html) {
  const set = new Set();
  for (const m of html.matchAll(/\?obr_progr=([0-9_]+)/g)) set.add(m[1]);
  return [...set].sort();
}

function extractGroups(html) {
  const re = /href='\?group=(\d+)(?:&[^']*)?' class='vt256'[^>]*data-nm='([^']*)'/g;
  const byId = new Map();
  for (const m of html.matchAll(re)) {
    byId.set(m[1], m[2].trim());
  }
  return [...byId.entries()].map(([id, name]) => ({ id, name }));
}

function extractVt244b(html) {
  const a = html.indexOf("<div class='vt244b'>");
  if (a < 0) return '';
  const b = html.indexOf("<div class='vt245'>", a);
  if (b < 0) return '';
  return html.slice(a, b);
}

function splitVt244Rows(vt244b) {
  const parts = vt244b.split("<div class='vt244'>");
  return parts.slice(1).map((p) => p.trimEnd());
}

function balancedDivInner(html, startIdx, openTag) {
  const i = html.indexOf(openTag, startIdx);
  if (i < 0) return { end: startIdx, inner: '' };
  const j = i + openTag.length;
  let depth = 1;
  let k = j;
  while (depth > 0 && k < html.length) {
    const nextOpen = html.indexOf('<div', k);
    const nextClose = html.indexOf('</div>', k);
    if (nextClose < 0) break;
    if (nextOpen !== -1 && nextOpen < nextClose) {
      depth += 1;
      k = nextOpen + 4;
    } else {
      depth -= 1;
      k = nextClose + 6;
    }
  }
  return { end: k, inner: html.slice(j, k - 6) };
}

function parseLessonInner(inner) {
  const subjM = inner.match(/<div class='vt240'>\s*([\s\S]*?)\s*<\/div>/);
  const subject = subjM
    ? subjM[1]
        .replace(/<[^>]+>/g, '')
        .replace(/\s+/g, ' ')
        .trim()
    : '';

  let teacherFull = '';
  let teacherShort = '';
  const tM = inner.match(/<span class='teacher'[^>]*title='([^']*)'[^>]*>([^<]*)<\/span>/);
  if (tM) {
    teacherFull = tM[1].split(';')[0].trim();
    teacherShort = tM[2].replace(/\s+/g, ' ').trim();
  }

  const audM = inner.match(/ауд\.:\s*([^;<]+)(?:;\s*([^<]*))?/);
  const room = audM ? audM[1].replace(/\s+/g, ' ').trim() : '';
  const building = audM?.[2] ? audM[2].replace(/\s+/g, ' ').trim() : '';

  const typeM = inner.match(/<div class='vt243[^']*'>\s*([\s\S]*?)\s*<\/div>/);
  const rawType = typeM
    ? typeM[1]
        .replace(/<[^>]+>/g, '')
        .replace(/\s+/g, ' ')
        .trim()
    : '';

  return { subject, teacherFull, teacherShort, room, building, rawType };
}

function normalizeLessonType(raw) {
  if (!raw) return raw;
  if (raw.includes('Практические занятия')) return 'Практическое занятие';
  return raw;
}

function parseScheduleGrid(html) {
  const vt244b = extractVt244b(html);
  if (!vt244b) return [];
  const rows = splitVt244Rows(vt244b);
  const lessons = [];

  for (const row of rows) {
    const slotM = row.match(/<div class='vt283'>(\d+)<\/div><br \/>(\d{2}:\d{2})<br \/>(\d{2}:\d{2})/);
    if (!slotM) continue;
    const slotNum = Number(slotM[1], 10);
    const startTime = slotM[2];
    const endTime = slotM[3];

    for (let dayIdx = 1; dayIdx <= 6; dayIdx++) {
      const marker = `<div class='vt239 rasp-day rasp-day${dayIdx}'>`;
      const pos = row.indexOf(marker);
      if (pos < 0) continue;
      const rest = row.slice(pos + marker.length).trimStart();
      if (rest.startsWith('</div>')) continue;

      const { inner } = balancedDivInner(row, pos, "<div class='vt258'>");
      if (!inner) continue;

      const parsed = parseLessonInner(inner);
      if (!parsed.subject) continue;

      lessons.push({
        day_of_week: dayIdx - 1,
        slot_number: slotNum,
        start_time: startTime,
        end_time: endTime,
        subject_name: parsed.subject,
        lesson_type: normalizeLessonType(parsed.rawType),
        teacher_full: parsed.teacherFull,
        teacher_short: parsed.teacherShort,
        classroom_name: parsed.room || null,
        building: parsed.building || null,
      });
    }
  }

  return lessons;
}

function teacherUuidFor(short, full, assignNew) {
  const s = (short || '').trim();
  if (s && LEGACY_TEACHER_UUID.has(s)) return LEGACY_TEACHER_UUID.get(s);
  const key = (full || s || '').trim();
  if (!key) return null;
  return assignNew(key);
}

function mainTeacherRegistry() {
  const keyToUuid = new Map();

  function assignNew(fullName) {
    const k = fullName.trim();
    if (keyToUuid.has(k)) return keyToUuid.get(k);
    const hex = createHash('sha256').update(k).digest('hex').slice(0, 12);
    const id = `d0000000-0000-5000-8000-${hex}`;
    keyToUuid.set(k, id);
    return id;
  }

  return { keyToUuid, assignNew };
}

function registerClassroom(room, building, classRegistry) {
  const k = `${building || ''}\0${room || ''}`;
  if (classRegistry.has(k)) return;
  const n = classRegistry.size + 1;
  const hex = n.toString(16).padStart(12, '0');
  const id = `c0000000-0000-4000-8000-${hex}`;
  classRegistry.set(k, id);
}

function chunk(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

async function main() {
  console.error('Fetching index…');
  const indexHtml = await fetchText(BASE);
  const programs = extractPrograms(indexHtml);
  console.error(`Programs: ${programs.length}`);

  const allPairs = [];
  const seenGroupId = new Set();
  for (const op of programs) {
    await sleep(120);
    const phtml = await fetchText(`${BASE}?obr_progr=${encodeURIComponent(op)}`);
    const groups = extractGroups(phtml);
    let added = 0;
    for (const g of groups) {
      if (seenGroupId.has(g.id)) continue;
      seenGroupId.add(g.id);
      allPairs.push({ obr_progr: op, ...g });
      added += 1;
    }
    console.error(`  obr_progr=${op} → ${groups.length} groups (${added} new)`);
  }

  console.error(`Total group rows: ${allPairs.length}`);

  const registry = mainTeacherRegistry();
  const { assignNew } = registry;

  const classRegistry = new Map();
  const templateRows = [];

  const semesterId = 'a0000000-0000-0000-0000-000000000001';

  for (let i = 0; i < allPairs.length; i++) {
    const { obr_progr, id: gid, name: groupName } = allPairs[i];
    const q = (d) => `${BASE}?obr_progr=${encodeURIComponent(obr_progr)}&group=${gid}&date=${d}`;

    await sleep(120);
    const h1 = await fetchText(q(WEEK1_MONDAY));
    await sleep(120);
    const h2 = await fetchText(q(WEEK2_MONDAY));

    for (const [weekNum, html] of [
      [1, h1],
      [2, h2],
    ]) {
      const lessons = parseScheduleGrid(html);
      for (const L of lessons) {
        const tid = teacherUuidFor(L.teacher_short, L.teacher_full, assignNew);
        const classroomLabel = formatClassroomLabel(L.building, L.classroom_name);
        if (classroomLabel) {
          registerClassroom(L.classroom_name, L.building, classRegistry);
        }
        templateRows.push({
          semester_id: semesterId,
          week_number: weekNum,
          day_of_week: L.day_of_week,
          slot_number: L.slot_number,
          start_time: L.start_time,
          end_time: L.end_time,
          subject_name: L.subject_name,
          lesson_type: L.lesson_type,
          teacher_id: tid,
          group_name: groupName,
          classroom_name: classroomLabel,
        });
      }
    }

    if ((i + 1) % 10 === 0) {
      console.error(`Progress: ${i + 1}/${allPairs.length} groups`);
    }
  }

  const idToName = new Map();
  for (const [short, uuid] of LEGACY_TEACHER_UUID) {
    idToName.set(uuid, short);
  }
  for (const [fullKey, uuid] of registry.keyToUuid) {
    if (!idToName.has(uuid)) idToName.set(uuid, fullKey);
  }

  const uniqueTeachers = [...idToName.entries()]
    .map(([id, name]) => ({ id, name }))
    .sort((a, b) => a.id.localeCompare(b.id));

  const classRows = [];
  for (const [k, cid] of classRegistry) {
    const [buildingRaw, roomRaw] = k.split('\0');
    const nm = formatClassroomLabel(buildingRaw, roomRaw);
    if (!nm) continue;
    classRows.push({ id: cid, name: nm, building: buildingRaw || null });
  }
  classRows.sort((a, b) => (a.name || '').localeCompare(b.name || '', 'ru'));

  const teachersSql = [
    seedFileHeader('teachers'),
    '-- teacher_cache',
    'INSERT INTO teacher_cache (id, name) VALUES',
    uniqueTeachers.map((t) => `    (${sqlStr(t.id)}, ${sqlStr(t.name)})`).join(',\n'),
    'ON CONFLICT (id) DO UPDATE SET name = EXCLUDED.name;',
  ].join('\n');

  const semesterSql = [
    seedFileHeader('semester'),
    '-- semester',
    'INSERT INTO semester (id, name, start_date, end_date, cycle_anchor_date, is_active)',
    `VALUES (${sqlStr(semesterId)}, ${sqlStr('Весна 2026')}, '2026-01-16', '2026-04-15', ${sqlStr(WEEK1_MONDAY)}, TRUE)`,
    'ON CONFLICT (id) DO NOTHING;',
  ].join('\n');

  const classroomsSql =
    classRows.length > 0
      ? [
          seedFileHeader('classrooms'),
          '-- classroom',
          'INSERT INTO classroom (id, name, building) VALUES',
          classRows.map((c) => `    (${sqlStr(c.id)}, ${sqlStr(c.name)}, ${sqlStr(c.building)})`).join(',\n'),
          'ON CONFLICT (id) DO UPDATE SET building = EXCLUDED.building;',
        ].join('\n')
      : [seedFileHeader('classrooms'), '-- classroom (нет строк в этом прогоне)'].join('\n');

  const scheduleParts = [
    seedFileHeader('schedule_template'),
    '-- schedule_template: id = DEFAULT gen_random_uuid(); ON CONFLICT ON CONSTRAINT uq_template_slot',
    '',
  ];
  const BATCH = 150;
  for (const part of chunk(templateRows, BATCH)) {
    scheduleParts.push(
      'INSERT INTO schedule_template (semester_id, week_number, day_of_week, slot_number, start_time, end_time, subject_name, lesson_type, teacher_id, group_name, classroom_name) VALUES',
    );
    scheduleParts.push(
      part
        .map(
          (r) =>
            `    (${sqlStr(r.semester_id)}, ${r.week_number}, ${r.day_of_week}, ${r.slot_number}, ${sqlStr(r.start_time)}, ${sqlStr(r.end_time)}, ${sqlStr(r.subject_name)}, ${sqlStr(r.lesson_type)}, ${r.teacher_id ? sqlStr(r.teacher_id) : 'NULL'}, ${sqlStr(r.group_name)}, ${sqlStr(r.classroom_name)})`,
        )
        .join(',\n'),
    );
    scheduleParts.push('ON CONFLICT ON CONSTRAINT uq_template_slot DO NOTHING;');
    scheduleParts.push('');
  }
  const scheduleSql = scheduleParts.join('\n').trimEnd() + '\n';

  const sessionSql = `${seedFileHeader('session_event')}${SESSION_EVENT_SQL}\n`;

  mkdirSync(MIGRATIONS_DIR, { recursive: true });
  writeFileSync(join(MIGRATIONS_DIR, '001_seed_teachers.sql'), teachersSql + '\n', 'utf8');
  writeFileSync(join(MIGRATIONS_DIR, '002_seed_semester.sql'), semesterSql + '\n', 'utf8');
  writeFileSync(join(MIGRATIONS_DIR, '003_seed_classrooms.sql'), classroomsSql + '\n', 'utf8');
  writeFileSync(join(MIGRATIONS_DIR, '004_seed_schedule_template.sql'), scheduleSql, 'utf8');
  writeFileSync(join(MIGRATIONS_DIR, '005_seed_session_event.sql'), sessionSql, 'utf8');
  console.error(`Wrote ${MIGRATIONS_DIR}/001…005 (${templateRows.length} schedule_template rows)`);

  mkdirSync(dirname(OUT_CSV), { recursive: true });
  const csvLines = [];
  for (const [fullKey, uuid] of registry.keyToUuid) {
    if (LEGACY_UUID_SET.has(uuid)) continue;
    csvLines.push(`${uuid}|${fullKey}`);
  }
  csvLines.sort();
  writeFileSync(OUT_CSV, csvLines.join('\n') + '\n', 'utf8');
  console.error(`Wrote ${OUT_CSV} (${csvLines.length} new teachers)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
